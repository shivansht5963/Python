# DICTIONARY

# Python dictionary is an unordered collection of items.

# Each item of a dictionary has a key/value pair.

# The keys pair with values using a colon (:) while the commas work as a separator for the elements.

# The entire dictionary object uses curly braces {} to enclose itself.

# The value in a dictionary can be of any valid Python data type. But the keys have a constraint to be of any immutable data types such as a string, a number, or a tuple.

